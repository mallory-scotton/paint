///////////////////////////////////////////////////////////////////////////////
/// \brief Convert uppercased character into lowercased character
///
/// \param c    The character to convert
///
/// \return The converted character
///
///////////////////////////////////////////////////////////////////////////////
int my_tolower(int c)
{
    if (c >= 'A' && c <= 'Z')
        return (c + 32);
    return (c);
}
