#include "rpg.h"

///////////////////////////////////////////////////////////////////////////////
/// \brief Print a null-terminated string to the standard error output.
///
/// \param str  Pointer to the null-terminated string to be printed.
///
///////////////////////////////////////////////////////////////////////////////
void my_puterr(cstring str)
{
    write(2, str, my_strlen(str));
}
