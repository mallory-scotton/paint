#include "rpg.h"

///////////////////////////////////////////////////////////////////////////////
/// \brief Print a null-terminated string to the standard output.
///
/// \param str  Pointer to the null-terminated string to be printed.
///
///////////////////////////////////////////////////////////////////////////////
void my_putstr(cstring str)
{
    write(1, str, my_strlen(str));
}
