///////////////////////////////////////////////////////////////////////////////
/// \brief Convert all uppercase letters in a string to lowercase
///
/// \param str      The string to convert.
///
/// \return A pointer to the modified string
///
///////////////////////////////////////////////////////////////////////////////
string my_strlwr(string str)
{
    string s = str;

    for (; *str; ++str)
        *str = my_tolower(*str);
    return (s);
}
