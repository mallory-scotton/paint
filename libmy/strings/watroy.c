///////////////////////////////////////////////////////////////////////////////
/// \brief Deallocate memory for a null-terminated array of strings
///
/// \param wa  Pointer to the null-terminated array of strings
///
///////////////////////////////////////////////////////////////////////////////
void my_watroy(warray wa)
{
    RETURN(wa == NULL, (void)0);
    for (ulong i = 0; wa[i]; i++)
        FREE(wa[i]);
    FREE(wa);
}
