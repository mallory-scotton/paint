#include "paint.h"

///////////////////////////////////////////////////////////////////////////////
/// \brief Handles erasing using the eraser tool.
///
/// \return None.
///
///////////////////////////////////////////////////////////////////////////////
void use_eraser_tool(void)
{
    sfColor tmpColor = Tool->color;

    Tool->color = sfTransparent;
    use_pencil_tool();
    Tool->color = tmpColor;
}
